package First;

public class Calculator implements Operations{


    public int plus(int a, int b) {
        return a+b;
    }

    public int minus(int a, int b) {
        return a-b;
    }

    public int mnoj(int a, int b) {
        return a*b;
    }

    public int delit(int a, int b) {
        return a/b;
    }

}
